const MORSE_TABLE = {
  ".-": "a",
  "-...": "b",
  "-.-.": "c",
  "-..": "d",
  ".": "e",
  "..-.": "f",
  "--.": "g",
  "....": "h",
  "..": "i",
  ".---": "j",
  "-.-": "k",
  ".-..": "l",
  "--": "m",
  "-.": "n",
  "---": "o",
  ".--.": "p",
  "--.-": "q",
  ".-.": "r",
  "...": "s",
  "-": "t",
  "..-": "u",
  "...-": "v",
  ".--": "w",
  "-..-": "x",
  "-.--": "y",
  "--..": "z",
  ".----": "1",
  "..---": "2",
  "...--": "3",
  "....-": "4",
  ".....": "5",
  "-....": "6",
  "--...": "7",
  "---..": "8",
  "----.": "9",
  "-----": "0"
};

function decode(expr) {
  const arr = [];
  let i = 0;

  while (i != expr.length) {
    arr.push(
      expr
        .slice(i, i + 10)
        .replace(/^0+/, "")
        .replace(/10/g, ".")
        .replace(/11/g, "-")
    );
    i += 10;
  }

  return arr
    .map(item => (item === "**********" ? " " : MORSE_TABLE[item]))
    .join("");
}

module.exports = {
  decode
};
